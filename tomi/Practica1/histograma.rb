def histograma(arr)
	max = arr.max
	min = arr.min
	min.upto max do |n|
		count = arr.count(n)
		str = "*" * count
		puts "#{n}: " + str
	end
end

def histograma_v(arr)
	max = arr.max
	min = arr.min
	min.upto max do |n|
		count = arr.count(n)
		str = "*" * count
		puts "#{n}: " + str
	end
end

